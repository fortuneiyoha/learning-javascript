// Arithmetic Operations

// Addition
var sum = 10 + 2;
console.log(sum); // Outputs: 12

// Subtraction
var difference = 10 - 2;
console.log(difference); // Outputs: 8

// Multiplication
var product = 10 * 2;
console.log(product); // Outputs: 20

// Division
var quotient = 10 / 2;
console.log(quotient); // Outputs: 5
